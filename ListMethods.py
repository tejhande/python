# List Methods
#Created A Random List
list = [10, 60, 50, 90, 20, 30, ] 
print(list)

list.append(40)     # Adds 40 At The End Of The List
print(list)

list.insert(1,70)   # Inserting 70 at Index 1
print(list)

list.sort()         # Sorts The List
print(list)

list.reverse()      # Reverses The List
print(list)

list.pop(2)         # Removes Index 2 element with pop
print(list)

list.remove(50)     # Removes 50 From List
print(list)  


